const html = document.documentElement;
const languageButtons = document.querySelectorAll(".lang-btn");
const translatableElements = document.querySelectorAll("[data-i18n]");

function setLanguage(language) {
  const dictionary = translations[language] || translations.en;
  html.lang = language;
  html.dir = language === "ar" ? "rtl" : "ltr";
  document.body.classList.toggle("is-ar", language === "ar");

  translatableElements.forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.innerHTML = dictionary[key];
    }
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  document.title =
    language === "ar"
      ? "جوراند | شبك فايبر جلاس بجودة تصدير"
      : "JORAND | Export Quality Fiberglass Mesh";

  localStorage.setItem("jorand-language", language);
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

const storedLanguage = localStorage.getItem("jorand-language");
const preferredLanguage = navigator.language && navigator.language.startsWith("ar") ? "ar" : "en";
setLanguage(storedLanguage || preferredLanguage);
